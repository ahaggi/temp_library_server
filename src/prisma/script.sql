CREATE TABLE Book (
  id TEXT not null,
  title TEXT not null,
  isbn TEXT not null,
  pages INTEGER not null,
  chapters INTEGER not null,
  price REAL not null,
  description TEXT,
  imgUri TEXT,
  unique(isbn),
  primary key (id)
);

CREATE TABLE Author (
  id TEXT not null,
  name TEXT not null,
  email TEXT not null,
  about TEXT ,
  imgUri TEXT,
  unique(email),
  primary key (id)
);

CREATE TABLE _BooksToAuthors (
  id TEXT not null,
  bookId TEXT not null,
  authorId TEXT not null,
  primary key (id),
  foreign key (bookId) references Book(id) ON DELETE CASCADE,
  foreign key (authorId) references Author(id)  ON DELETE CASCADE
);

CREATE TABLE Reader (
  id TEXT not null,
  name TEXT not null,
  email TEXT not null,
  costumerId TEXT NOT null,
  address TEXT,
  phone TEXT,
  imgUri TEXT,
  unique(email),
  unique(phone),
  primary key (id)
);

CREATE TABLE _BooksToReaders (
  id TEXT not null,
  borrowDate TEXT not null,
  returnDate TEXT not null,
  returned BOOLEAN not null,
  bookId TEXT not null,
  readerId TEXT not null,
  primary key (id),
  foreign key (bookId) references Book(id) ON DELETE CASCADE,
  foreign key (readerId) references Reader(id)  ON DELETE CASCADE
);

CREATE TABLE Storage (
  id TEXT not null,
  bookId TEXT not null,
  quantity INTEGER not null,
  borrowedQuantity INTEGER not null CHECK (quantity >= borrowedQuantity),
  primary key (id),
  foreign key (bookId) references Book(id)  ON DELETE CASCADE
);


